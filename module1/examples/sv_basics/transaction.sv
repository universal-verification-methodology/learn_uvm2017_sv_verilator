            // 'super' refers to the parent class
            // This initializes inherited fields (id, data, timestamp)
            super.new(name);
            // Initialize write-specific fields
            address = 0;                    // Default address is 0
            transaction_type = "WRITE";     // Set transaction type identifier
        endfunction
        
        /**
         * Override string representation
         * 
         * This method overrides the parent class's convert2string() method.
         * Provides a more specific string representation for write transactions.
         * 
         * @return Formatted string with write transaction fields including address
         */
        function string convert2string();
            // Format string specific to write transactions
            // Includes address field which is unique to read/write transactions
            return $sformatf("WriteTransaction(id=%0d, address=0x%0h, data=0x%0h)", 
                           id, address, data);
        endfunction
        
        /**
         * Get write address
         * 
         * Accessor method to retrieve the memory address for this write transaction.
         * 
         * @return 16-bit address value
         */
        function bit [15:0] get_address();
            // Return the instance's address field
            return address;
        endfunction
    endclass  // End of WriteTransaction class definition

// End of package definition
endpackage

/**
 * Test program to demonstrate transaction classes
 * 
