// ============================================================================
// PACKAGE DECLARATION
// ============================================================================
// Package declaration: creates a namespace for related classes and types
// Packages allow code organization and prevent naming conflicts
// 
// PACKAGE BENEFITS:
//   - Namespace isolation: Prevents name collisions
//   - Code reuse: Can be imported into multiple modules
//   - Organization: Groups related functionality together
//   - Compilation: Packages are compiled once, used many times
// 
// IMPORT SYNTAX:
//   - 'import transaction_pkg::*;' - Import all items (wildcard)
//   - 'import transaction_pkg::Transaction;' - Import specific item
//   - 'transaction_pkg::Transaction' - Use fully qualified name (no import needed)
// 
// 'transaction_pkg' is the package name, can be imported with 'import transaction_pkg::*;'
package transaction_pkg;
    // ============================================================================
    // BASE TRANSACTION CLASS
    // ============================================================================
    /**
     * Base transaction class for verification.
     * 
     * This class demonstrates:
     * - Class definition and object-oriented concepts
     * - Instance variables (object-specific data)
     * - Static variables (class-wide data)
     * - Methods (functions and tasks)
     * - Special methods (copy, compare, convert2string)
     * 
     * TRANSACTION CLASS PURPOSE:
     *   A transaction class is the fundamental building block for verification.
     *   It encapsulates data and operations related to a single bus transaction.
     *   Transactions flow through the verification environment:
     *     1. Generated by sequences/sequences
     *     2. Processed by drivers
     *     3. Monitored by monitors
     *     4. Compared in scoreboards
     * 
     * CLASS vs MODULE:
     *   - Class: Dynamic, can create multiple instances, supports inheritance
     *   - Module: Static, instantiated at compile time, no inheritance
     *   - Classes are used for testbench (verification), modules for DUT (design)
     */
    class Transaction;
        // ========================================================================
        // STATIC (CLASS) VARIABLES
        // ========================================================================
        // Class variable (static): shared across all instances of the class
        // 
        // STATIC VARIABLE SEMANTICS:
        //   - 'static' means this variable belongs to the class, not individual objects
        //   - All Transaction objects share the same id_counter
        //   - Access: Transaction::id_counter (class scope) or Transaction.id_counter (instance)
        //   - Lifetime: Exists for entire simulation (not tied to object lifetime)
        //   - Initialization: Initialized once when class is first referenced
        // 
        // USE CASE: Unique ID generation
        //   - Each transaction needs a unique identifier
        //   - Static counter ensures uniqueness across all instances
        //   - Thread-safe in SystemVerilog (no race conditions in single-threaded simulation)
        // 
        // Initialized to 0, increments each time a new Transaction is created
        static int id_counter = 0;
        
        // ========================================================================
        // INSTANCE VARIABLES (OBJECT DATA MEMBERS)
        // ========================================================================
        // Instance variables: each object has its own copy of these variables
        // These are the "data members" of the class
        // 
        // DATA TYPES:
        //   - 'int': 32-bit signed integer (-2^31 to 2^31-1)
        //   - 'bit [31:0]': 32-bit unsigned bit vector (0 to 2^32-1)
        //   - 'int unsigned': 32-bit unsigned integer (0 to 2^32-1)
        // 
        // 'int' is a 32-bit signed integer type
        int id;              // Unique identifier for this transaction instance
                              // Assigned from static id_counter in constructor
                              // Used for tracking and debugging transactions
        
        // 'bit [31:0]' declares a 32-bit unsigned bit vector (bits 31 down to 0)
        // Bit vector syntax: [MSB:LSB] where MSB=Most Significant Bit, LSB=Least Significant
        bit [31:0] data;      // 32-bit data field for transaction payload
                              // Stores the actual data being transferred
                              // Range: 0x00000000 to 0xFFFFFFFF
        
        // 'int unsigned' is a 32-bit unsigned integer
        int unsigned timestamp;  // Timestamp when transaction was created/processed
                                 // Used for timing analysis and debugging
                                 // Typically set using $time or simulation time
                                 // Unsigned because time is always positive
        
        // ========================================================================
        // CONSTRUCTOR
        // ========================================================================
        /**
         * Constructor: called automatically when object is created with 'new()'
         * 
         * CONSTRUCTOR PURPOSE:
         *   - Initialize object to a known good state
         *   - Allocate resources if needed
         *   - Set default values for instance variables
         * 
         * CONSTRUCTOR CALL:
         *   Transaction txn;           // Declare handle (null pointer)
         *   txn = new();               // Create object, calls constructor
         *   txn = new("MyTxn");        // Create with name parameter
         * 
         * @param name Optional string parameter for object name (not used in this example)
         *              Default value "Transaction" allows calling new() without arguments
         *              In UVM, this would be used for hierarchical naming
         */
        function new(string name = "Transaction");
            // Increment the shared class counter
            // This ensures each transaction gets a unique ID
            // 
            // ID GENERATION ALGORITHM:
            //   1. First transaction: id_counter=0 -> increment to 1 -> id=1
            //   2. Second transaction: id_counter=1 -> increment to 2 -> id=2
            //   3. And so on...
            // 
            // IMPORTANT: Pre-increment (++id_counter) vs post-increment (id_counter++)
            //   - Current: Post-increment (increment after use)
            //   - Result: First transaction gets id=1 (not 0)
            //   - Alternative: Pre-increment would give id=0 for first transaction
            id_counter++;
            
            // Assign the incremented counter value to this instance's ID
            // This gives each transaction a unique identifier
            // Useful for: Debugging, tracking, scoreboard matching
            id = id_counter;
            
            // Initialize data field to zero
            // Default value: 0x00000000
            // User should set this to actual transaction data after creation
            data = 0;
            
            // Initialize timestamp to zero (can be set later with set_timestamp)
            // Default value: 0 (indicates not yet timestamped)
            // Typically set when transaction is processed: set_timestamp($time)
            timestamp = 0;
        endfunction
        
        // ========================================================================
        // COPY METHOD (DEEP COPY)
        // ========================================================================
        /**
         * Copy method: creates a deep copy of another transaction
         * 
         * COPY SEMANTICS:
         *   - Deep copy: Copies all data members (not just the handle)
         *   - Shallow copy: Would only copy the handle (not implemented here)
         *   - This method modifies 'this' object (the object calling copy)
         * 
         * USE CASES:
         *   - Cloning transactions before sending to multiple destinations
         *   - Saving transaction state for later comparison
         *   - Creating backup copies before modification
         *   - Implementing transaction queues (need copies, not references)
         * 
         * UVM EQUIVALENT: uvm_object::copy(uvm_object rhs)
         * 
         * This is a common pattern in verification for duplicating transactions.
         * Used when you need to preserve a transaction's state.
         * 
         * @param rhs Right-hand side: the transaction to copy from
         *            'rhs' is a common naming convention (right-hand side)
         *            Represents the source object being copied
         */
        function void copy(Transaction rhs);
            // Null pointer check: prevent errors if rhs is null
            // 
            // NULL POINTER SAFETY:
            //   - Always check for null before accessing object members
            //   - Prevents simulation crashes from null pointer dereference
            //   - Provides meaningful error messages for debugging
            // 
            // 'rhs == null' checks if the pointer is null (no object assigned)
            // In SystemVerilog, handles can be null if:
            //   - Declared but never assigned: Transaction txn; (txn is null)
            //   - Explicitly set to null: txn = null;
            if (rhs == null) begin
                // Print error message and return early
                // '$error' is a SystemVerilog reporting macro (severity: ERROR)
                // Other reporting macros: $info, $warning, $fatal
                $error("Copy failed: null pointer");
                // Exit function early to prevent null pointer dereference
                // 'return' exits the function immediately
                return;
            end
            
            // Copy all instance variables from source to destination
            // This is a member-by-member copy (deep copy)
            // 
            // COPY OPERATION:
            //   - 'rhs.id' accesses the id field of the right-hand side object
            //   - 'id' (without prefix) refers to 'this.id' (the current object)
            //   - Assignment copies the value (not the reference)
            // 
            // NOTE: For complex objects (arrays, nested objects), you may need
            //       to implement deeper copying logic
            id = rhs.id;                    // Copy unique identifier
            data = rhs.data;                // Copy 32-bit data field
            timestamp = rhs.timestamp;       // Copy timestamp value
            
            // NOTE: Static variables (id_counter) are NOT copied
            //       They belong to the class, not individual objects
        endfunction
        
        // ========================================================================
        // COMPARE METHOD (EQUALITY CHECK)
        // ========================================================================
        /**
         * Compare method: checks if two transactions are equivalent
         * 
         * COMPARISON SEMANTICS:
         *   - Equality: Two transactions are equal if their key fields match
         *   - Key fields: id and data (timestamp is ignored)
         *   - Returns: 1 (true) if match, 0 (false) if mismatch
         * 
         * USE CASES:
         *   - Scoreboard: Compare expected vs actual transactions
         *   - Transaction matching: Find matching transactions in queues
         *   - Debugging: Verify transaction integrity
         *   - Test assertions: Check transaction correctness
         * 
         * UVM EQUIVALENT: uvm_object::compare(uvm_object rhs, uvm_comparer comparer)
         * 
         * This is used for checking expected vs actual transactions in scoreboards.
         * Returns 1 (true) if transactions match, 0 (false) otherwise.
         * 
         * @param rhs Right-hand side: the transaction to compare with
         *            'this' is the left-hand side (lhs), 'rhs' is right-hand side
         * @return 1 if transactions match (same id and data), 0 otherwise
         */
        function bit compare(Transaction rhs);
            // Null pointer check: if rhs is null, transactions cannot match
            // 
            // NULL HANDLING:
            //   - Comparing with null always returns false
            //   - This prevents null pointer dereference errors
            //   - Early return for efficiency
            if (rhs == null) return 0;  // Return false (0) if comparing with null
            
            // Compare both id and data fields
            // 
            // COMPARISON LOGIC:
            //   - 'id == rhs.id': Compare unique identifiers
            //   - 'data == rhs.data': Compare data payloads
            //   - '&&': Logical AND - both must be true for match
            //   - '==' is equality operator: returns 1 if equal, 0 if not
            // 
            // EQUALITY DEFINITION:
            //   - Two transactions are equal if they have the same id AND data
            //   - This is a strict equality (both fields must match)
            //   - Alternative: Could compare only data (ignore id)
            //   - Alternative: Could use fuzzy matching (tolerance for data)
            return (id == rhs.id) && (data == rhs.data);
            
            // Note: timestamp is not compared, only id and data matter for equality
            // 
            // DESIGN DECISION: Why ignore timestamp?
            //   - Timestamps represent when transaction was processed
            //   - Two identical transactions processed at different times should match
            //   - Timestamp is metadata, not part of transaction identity
            //   - If timestamp comparison needed, add separate method: compare_with_time()
        endfunction
        
        // ========================================================================
        // CONVERT2STRING METHOD (STRING REPRESENTATION)
        // ========================================================================
        /**
         * String conversion - equivalent to __str__ in Python or toString() in Java
         * 
         * STRING FORMATTING:
         *   - Provides human-readable representation of transaction
         *   - Essential for debugging and logging
         *   - Should include all key fields for identification
         * 
         * USE CASES:
         *   - Debug output: $display("Transaction: %s", txn.convert2string());
         *   - Log files: Write transaction details to log
         *   - Error messages: Include transaction info in error reports
         *   - Waveform annotation: Display transaction info in waveforms
         * 
         * UVM EQUIVALENT: uvm_object::convert2string()
         * 
         * This method provides a human-readable string representation of the transaction.
         * Used for debugging and logging purposes.
         * 
         * @return Formatted string containing transaction fields
         *         Format: "Transaction(id=<id>, data=0x<hex>, timestamp=<time>)"
         */
        function string convert2string();
            // '$sformatf' is SystemVerilog's sprintf function: formats string with placeholders
            // Similar to printf() in C or format() in Python
            // 
            // FORMAT SPECIFIERS:
            //   - '%0d': Decimal integer, no leading zeros (0 means minimal width)
            //   - '%0h': Hexadecimal integer, no leading zeros, lowercase
            //   - '%0H': Hexadecimal integer, uppercase
            //   - '%0x': Same as %0h (hexadecimal)
            //   - '%s': String
            //   - '%f': Floating point
            // 
            // FORMAT STRING BREAKDOWN:
            //   - "Transaction(id=%0d, data=0x%0h, timestamp=%0d)"
            //   - %0d (first): Formats id as decimal
            //   - %0h: Formats data as hexadecimal (with 0x prefix in literal)
            //   - %0d (second): Formats timestamp as decimal
            // 
            // ARGUMENTS:
            //   - id: Transaction ID (integer)
            //   - data: Transaction data (32-bit, displayed as hex)
            //   - timestamp: Transaction timestamp (integer)
            // 
            // Returns a formatted string with all transaction fields
            return $sformatf("Transaction(id=%0d, data=0x%0h, timestamp=%0d)", 
                           id, data, timestamp);
            
            // EXAMPLE OUTPUT:
            //   "Transaction(id=1, data=0x1234, timestamp=100)"
        endfunction
        
        // ========================================================================
        // SET_TIMESTAMP METHOD (MUTATOR)
        // ========================================================================
        /**
         * Set transaction timestamp
         * 
         * MUTATOR METHOD:
         *   - Allows modification of object state after creation
         *   - Provides controlled access to instance variables
         *   - Can add validation logic if needed
         * 
         * TIMESTAMP USAGE:
         *   - Track when transaction was created: set_timestamp($time)
         *   - Track when transaction was processed: set_timestamp($time)
         *   - Timing analysis: Calculate transaction latency
         *   - Debugging: Identify transaction timing issues
         * 
         * Allows setting the timestamp after object creation.
         * Useful for tracking when transactions are processed.
         * 
         * @param ts Unsigned integer timestamp value to assign
         *           Typically obtained from $time or $realtime
         *           Should be non-negative (unsigned ensures this)
         */
        function void set_timestamp(int unsigned ts);
            // Assign the provided timestamp value to the instance variable
            // 
            // ASSIGNMENT:
            //   - Direct assignment: timestamp = ts;
            //   - No validation: Could add range checking if needed
            //   - No side effects: Simple setter method
            timestamp = ts;
            
            // ALTERNATIVE IMPLEMENTATION (with validation):
            //   if (ts > MAX_TIMESTAMP) begin
            //       $warning("Timestamp exceeds maximum: %0d", ts);
            //   end
            //   timestamp = ts;
        endfunction
        
        // ========================================================================
        // GET_ID METHOD (ACCESSOR)
        // ========================================================================
        /**
         * Get transaction ID
         * 
         * ACCESSOR METHOD (GETTER):
         *   - Provides read-only access to instance variable
         *   - Encapsulation: Hides internal implementation
         *   - Can add logic (validation, formatting) if needed later
         * 
         * ENCAPSULATION BENEFITS:
         *   - If id storage changes, only this method needs updating
         *   - Can add logging: Log every ID access
         *   - Can add validation: Verify ID is valid before returning
         * 
         * Accessor method to retrieve the transaction's unique identifier.
         * Provides controlled access to the id field.
         * 
         * @return The transaction's unique ID (integer)
         *         Range: 1 to maximum (depends on id_counter)
         *         Never returns 0 (first transaction gets id=1)
         */
        function int get_id();
            // Return the instance's id field
            // 
            // RETURN SEMANTICS:
            //   - Returns by value (not by reference)
            //   - Caller gets a copy of the ID
            //   - Modifying return value doesn't affect object
            return id;
            
            // USAGE EXAMPLE:
            //   Transaction txn = new();
            //   int my_id = txn.get_id();  // Get ID value
            //   $display("ID: %0d", my_id);
        endfunction
    endclass  // End of Transaction class definition
    
    /**
     * Read transaction class.
     * 
     * Demonstrates inheritance and method overriding.
     * This class extends (inherits from) the base Transaction class.
     * It adds address-specific fields and methods for read operations.
     */
    class ReadTransaction extends Transaction;
        // Additional instance variables specific to read transactions
        // 'bit [15:0]' declares a 16-bit unsigned bit vector (bits 15 down to 0)
        bit [15:0] address;        // Memory address to read from (16-bit)
        // 'string' is a SystemVerilog string type (dynamic length)
        string transaction_type;  // Type identifier: "READ"
        
        /**
         * Constructor for ReadTransaction
         * 
         * Must call parent class constructor using 'super.new()'
         * 
         * @param name Optional string parameter (defaults to "ReadTransaction")
         */
        function new(string name = "ReadTransaction");
            // Call parent class (Transaction) constructor
            // 'super' refers to the parent class
            // This initializes inherited fields (id, data, timestamp)
            super.new(name);
            // Initialize read-specific fields
            address = 0;                    // Default address is 0
            transaction_type = "READ";     // Set transaction type identifier
        endfunction
        
        /**
         * Override string representation
         * 
         * This method overrides the parent class's convert2string() method.
         * Provides a more specific string representation for read transactions.
         * 
         * @return Formatted string with read transaction fields including address
         */
        function string convert2string();
            // Format string specific to read transactions
            // Includes address field which is unique to read/write transactions
            return $sformatf("ReadTransaction(id=%0d, address=0x%0h, data=0x%0h)", 
                           id, address, data);
        endfunction
        
        /**
         * Get read address
         * 
         * Accessor method to retrieve the memory address for this read transaction.
         * 
         * @return 16-bit address value
         */
        function bit [15:0] get_address();
            // Return the instance's address field
            return address;
        endfunction
    endclass  // End of ReadTransaction class definition
    
    /**
     * Write transaction class.
     * 
     * Demonstrates inheritance and method overriding.
     * This class extends (inherits from) the base Transaction class.
     * It adds address-specific fields and methods for write operations.
     */
    class WriteTransaction extends Transaction;
        // Additional instance variables specific to write transactions
        // 'bit [15:0]' declares a 16-bit unsigned bit vector (bits 15 down to 0)
        bit [15:0] address;        // Memory address to write to (16-bit)
        // 'string' is a SystemVerilog string type (dynamic length)
        string transaction_type;  // Type identifier: "WRITE"
        
        /**
         * Constructor for WriteTransaction
         * 
         * Must call parent class constructor using 'super.new()'
         * 
         * @param name Optional string parameter (defaults to "WriteTransaction")
         */
        function new(string name = "WriteTransaction");
