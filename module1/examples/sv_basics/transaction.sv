    // Import all items from the transaction_pkg package
    // 'import transaction_pkg::*;' makes all classes and types available
    // Without this, you would need to use 'transaction_pkg::Transaction'
    import transaction_pkg::*;
    
    // 'initial' block: executes once at simulation start (time 0)
    // This is the test program that demonstrates transaction class usage
    initial begin
        // Variable declarations: declare handles (pointers) to transaction objects
        // These are not objects yet, just handles that can point to objects
        // 'Transaction' is the base class type
        Transaction txn1, txn2;        // Two base transaction handles
        // 'ReadTransaction' is a derived class (inherits from Transaction)
        ReadTransaction read_txn;      // Read transaction handle
        // 'WriteTransaction' is a derived class (inherits from Transaction)
        WriteTransaction write_txn;   // Write transaction handle
        Transaction txn_copy;         // Handle for copied transaction
        
        // Display section header for test output
        // '$display' prints formatted text to console (like printf in C)
        // '====' creates a visual separator in the output
        $display("============================================================");
        $display("Module 1 Example 1.1: SystemVerilog Class Basics");
        $display("============================================================");
        // Empty line for readability
        $display();
        
        // Test 1: Create and use a base transaction object
        $display("1. Creating base transaction:");
        // 'new()' creates a new Transaction object and returns a handle
        // The handle is assigned to txn1, which now points to the object
        txn1 = new();
        // Access instance variable: set the data field to hexadecimal 0x1234
        // '32'h1234' is a 32-bit hexadecimal literal
        txn1.data = 32'h1234;
        // Call convert2string() method to get string representation
        // '%s' is string format specifier
        $display("   %s", txn1.convert2string());
        // Call get_id() method to retrieve the transaction ID
        // '%0d' formats integer in decimal without leading zeros
        $display("   Transaction ID: %0d", txn1.get_id());
        $display();  // Empty line
        
        // Test 2: Create and use a read transaction (demonstrates inheritance)
        $display("2. Creating read transaction (inheritance):");
        // Create a ReadTransaction object (inherits from Transaction)
        // This object has all Transaction fields (id, data, timestamp) plus address
        read_txn = new();
        // Set the address field (specific to ReadTransaction, not in base class)
        // '16'h1000' is a 16-bit hexadecimal literal (4096 in decimal)
        read_txn.address = 16'h1000;
        // Set the data field (inherited from Transaction base class)
        // '32'hDEAD' is a 32-bit hexadecimal literal
        read_txn.data = 32'hDEAD;
        // Call overridden convert2string() method
        // This calls ReadTransaction's version, not Transaction's version
        $display("   %s", read_txn.convert2string());
        // Call get_address() method (specific to ReadTransaction)
        // '%0h' formats integer in hexadecimal without leading zeros
        $display("   Address: 0x%0h", read_txn.get_address());
        $display();  // Empty line
        
        // Test 3: Create and use a write transaction (demonstrates inheritance)
        $display("3. Creating write transaction (inheritance):");
        // Create a WriteTransaction object (inherits from Transaction)
        // This object has all Transaction fields plus address
        write_txn = new();
        // Set the address field (specific to WriteTransaction)
        // '16'h2000' is a 16-bit hexadecimal literal (8192 in decimal)
        write_txn.address = 16'h2000;
        // Set the data field (inherited from Transaction base class)
        // '32'hBEEF' is a 32-bit hexadecimal literal
        write_txn.data = 32'hBEEF;
        // Call overridden convert2string() method
        // This calls WriteTransaction's version, not Transaction's version
        $display("   %s", write_txn.convert2string());
        // Call get_address() method (specific to WriteTransaction)
        $display("   Address: 0x%0h", write_txn.get_address());
        $display();  // Empty line
        
        // Test 4: Demonstrate copy method
        $display("4. Testing copy:");
        // Create a new Transaction object to hold the copy
        txn_copy = new();
        // Call copy() method: copies all fields from txn1 to txn_copy
        // This creates a duplicate transaction with same id, data, and timestamp
        txn_copy.copy(txn1);
        // Display both original and copy to verify they match
        $display("   Original: %s", txn1.convert2string());
        $display("   Copy:     %s", txn_copy.convert2string());
        $display();  // Empty line
        
        // Test 5: Demonstrate compare method
        $display("5. Testing compare:");
        // Create a second transaction for comparison
        txn2 = new();
        // Set data to same value as txn1
        txn2.data = 32'h1234;
        // Manually set ID to match txn1's ID
        // This makes txn2 identical to txn1 (same id and data)
        txn2.id = txn1.id;  // Same ID
        // Call compare() method: returns 1 if transactions match, 0 otherwise
        // txn1 and txn2 should match (same id and data)
        $display("   txn1 == txn2: %0d", txn1.compare(txn2));
        // Compare txn1 (base class) with read_txn (derived class)
        // This should return 0 (false) because they have different IDs
        $display("   txn1 == read_txn: %0d", txn1.compare(read_txn));
